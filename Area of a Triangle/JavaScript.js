function triArea(base, height) {
  const area = (base / 2) * height;
  return area;
}

const result1 = triArea(3, 2);
const result2 = triArea(5, 4);
const result3 = triArea(10, 10);
const result4 = triArea(0, 60);
const result5 = triArea(12, 11);

